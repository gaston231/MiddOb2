package com.stock;

public class Pedido {
	
	public long IdProducto;
	
	public int Cantidad;

	public long getIdProducto() {
		return IdProducto;
	}

	public void setIdProducto(long idProducto) {
		IdProducto = idProducto;
	}

	public int getCantidad() {
		return Cantidad;
	}

	public void setCantidad(int cantidad) {
		Cantidad = cantidad;
	}
	


}
